package day1;

public class i {
	public static void main(String[] args) {
		int a=20;
		System.out.println(a);
		a++;
		System.out.println(a);
		//post increment
		int b=30;
		System.out.println(b);
		++b;
		System.out.println(b);
		//pre increment
		int c=40;
		System.out.println(c);
		c--;
		System.out.println(c);
		//post decrement
		int d=50;
		System.out.println(d);
		--d;
		System.out.println(d);
		//pre decrement
	}

}
